def _build_sigungu_index():
    """정규 시군구(_NAMES) → 매칭 키. 정규 풀네임 외에 보조 키도 등록한다:
      - 두 단어 시군구('수원시 권선구') → 뒤 토큰('권선구')
      - 한 단어 시/군('파주시','가평군') → 어간('파주','가평')  (시/군 없이 말하는 경우)
    보조 키가 여러 정규명에 겹치거나 시도 약칭과 충돌하면 제외(추측 0)."""
    canon = {}          # 매칭키 → 정규 시군구명
    alt_owner = {}      # 보조키 → 정규명
    alt_amb = set()

    def add_alt(key, full):
        if not key or len(key) < 2 or key in SIDO_ALIAS:
            return
        if key in alt_owner and alt_owner[key] != full:
            alt_amb.add(key)
        else:
            alt_owner[key] = full

    for name in _NAMES.get("sigungu", []):
        name = (name or "").strip()
        if not name or name == "없음":
            continue
        canon[name] = name
        parts = name.split()
        if len(parts) > 1:
            add_alt(parts[-1], name)                 # 뒤 토큰
        elif name[-1] in ("시", "군"):
            add_alt(name[:-1], name)                 # 시/군 어간

    for key, full in alt_owner.items():
        if key not in alt_amb and key not in canon:
            canon[key] = full
    return canon


_SIGUNGU_CANON = _build_sigungu_index()

# 매칭은 항상 '긴 이름 우선'(부분문자열 과매칭 방지)
_SIGUNGU_KEYS = sorted(_SIGUNGU_CANON, key=len, reverse=True)
_SIDO_FULL_SORTED = sorted(set(_SIDO_FULL), key=len, reverse=True)
_SIDO_ALIAS_KEYS = sorted(SIDO_ALIAS, key=len, reverse=True)
_ALIAS_KEYS = sorted(ALIASES, key=len, reverse=True)


def _infer_sido(sigungu):
    """정규 시군구명 → 시도(모호하지 않은 것만). 못 정하면 None."""
    if not sigungu:
        return None
    if sigungu in _SIGUNGU_TO_SIDO:
        return _SIGUNGU_TO_SIDO[sigungu]
    head = sigungu.split()[0]          # "수원시 권선구" → "수원시"
    return _SIGUNGU_TO_SIDO.get(head)


def _detect_sido(t):
    """텍스트에서 시도를 독립 감지(정규명 우선, 그다음 약칭)."""
    for name in _SIDO_FULL_SORTED:
        if name in t:
            # 강원도/전라북도 등 구표기 → 현행 정규명으로 정규화
            return {"강원도": "강원특별자치도", "전라북도": "전북특별자치도"}.get(name, name)
    for alias in _SIDO_ALIAS_KEYS:
        if alias in t:
            return SIDO_ALIAS[alias]
    return None


def detect_region(text):
    """자유 텍스트 → {"sido": str|None, "sigungu": str|None}."""
    if not text or not str(text).strip():
        return {"sido": None, "sigungu": None}
    t = unicodedata.normalize("NFKC", str(text))

    sigungu = None
    alias_sido = None

    # 1) 정규 시군구 직접 매칭(긴 이름 우선) — 데이터가 진실, 가장 구체적인 것 채택
    for name in _SIGUNGU_KEYS:
        if name in t:
            sigungu = _SIGUNGU_CANON[name]
            break

    # 2) 정규 목록에 없으면 별칭(랜드마크·약칭)으로 보강 — sido 까지 같이 얻는다
    if not sigungu:
        for key in _ALIAS_KEYS:
            if key in t:
                alias_sido, sigungu = ALIASES[key]
                break

    # 3) 시도: 시군구에서 얻은 시도를 우선(텍스트 부분문자열보다 신뢰 — "해운대구"의
    #    '대구' 오매칭 방지). 없으면 텍스트에서 독립 감지("서울 얘기"처럼 구 단서 없는 경우).
    sido = alias_sido or _infer_sido(sigungu) or _detect_sido(t)

    return {"sido": sido, "sigungu": sigungu}


